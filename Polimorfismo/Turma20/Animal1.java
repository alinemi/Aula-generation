package Turma20;

public class Animal1 {
	private String nome;
	private String idade;
	private String som;
	private String correr;
	
	public String getNome() {
		return nome;
	}
	public void setNome(final String nome) {
		this.nome = nome;
	}
	public String getIdade() {
		return idade;
	}
	public void setIdade(final String idade) {
		this.idade = idade;
	}
	public String getSom() {
		return som;
	}
	public void setSom(final String som) {
		this.som = som;
	}
	public String getCorrer() {
		return correr;
	}
	public void setCorrer(final String correr) {
		this.correr = correr;
	}
	
	

}
